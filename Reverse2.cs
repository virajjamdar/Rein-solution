using System;
using System.Collections.Generic;
using System.Text;

namespace project2
{
    class Reverse2
    {
        static void Main(string[] args)
        {
            
                for (int i = 4; i >= 1; i--)
                {

                    for (int j = 1; j <= i; j++)
                    {
                        Console.Write(i);
                    }
                    Console.WriteLine();
                }

            
        }
    }
